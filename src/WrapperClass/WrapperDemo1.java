package WrapperClass;


//Wrapper class is a Java class whose object wraps or contains a primitive data type
//When we create a object of the wrapper class, then it contains a field and in that field we can store a primitive data type
public class WrapperDemo1 {

    public static void main(String[] args) {

        //Integer is a Wrapper class of int
        Integer obj1= new Integer(20);


        Integer obj2=10;

        System.out.println(obj1);
    }
}
